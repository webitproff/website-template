export function getStaticPaths(){
  
  return {
    paths: ourWorks.map(({ path }) => (
      { params: { path } }
    )),
    fallback: false
  }
}

export function getStaticProps({ params }){
  
  return {
    props: {
      ...ourWorks.find(({ path }) => path === params.path)
    }
  }
}